const checkIdToNameMap: Record<number, string> = {};
const checkNameToIdMap: Record<string, number> = {};

checkNames.forEach((checkName, i) => {
  checkIdToNameMap[i] = checkName;
  checkNameToIdMap[checkName] = i;
});

export function checkIdToName(checkId: number) {
  if (Object.prototype.hasOwnProperty.call(checkIdToNameMap, checkId)) {
    return checkIdToNameMap[checkId];
  }
  return null;
}

export function checkNameToId(checkName: string) {
  if (Object.prototype.hasOwnProperty.call(checkNameToIdMap, checkName)) {
    return checkNameToIdMap[checkName];
  }
  return -1;
}

export const alphabeticalCheckIds: number[] = checkNames.sort().map(checkNameToId);
